-- ============================================
-- WATCH HISTORY TABLE
-- ============================================
-- Tracks user's watch progress for each episode
-- Supports multiple providers (anilist, mal, kitsu)
-- Supports multiple sources (different streaming servers)
-- ============================================

CREATE TABLE public.watch_history (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  
  -- User & Anime identification
  user_id TEXT NOT NULL,
  anime_id TEXT NOT NULL,
  provider TEXT NOT NULL DEFAULT 'anilist',
  
  -- Episode identification
  episode_id TEXT NOT NULL,
  episode_number INTEGER,
  episode_title TEXT,
  
  -- Anime metadata (for display without additional API calls)
  anime_title TEXT,
  
  -- Streaming source (server name: kaa-manifest, kaido, gojowtf, etc.)
  -- This tells which server successfully streamed this episode
  source TEXT NOT NULL DEFAULT 'default',
  
  -- Progress tracking
  position DOUBLE PRECISION NOT NULL DEFAULT 0,
  duration DOUBLE PRECISION NOT NULL DEFAULT 0,
  completed BOOLEAN NOT NULL DEFAULT false,
  
  -- Timestamps
  updated_at BIGINT NOT NULL,
  created_at BIGINT DEFAULT EXTRACT(EPOCH FROM NOW()) * 1000
);

-- Unique constraint: one progress entry per user+anime+episode+source combination
CREATE UNIQUE INDEX watch_history_user_anime_episode_source_key
  ON public.watch_history (user_id, anime_id, provider, episode_id, source);

-- Index for fast "continue watching" queries (most recent incomplete episodes)
CREATE INDEX watch_history_user_updated_at_idx
  ON public.watch_history (user_id, updated_at DESC);

-- Index for fast anime-specific history queries
CREATE INDEX watch_history_user_anime_idx
  ON public.watch_history (user_id, anime_id, provider);

-- Index for completed episodes queries
CREATE INDEX watch_history_completed_idx
  ON public.watch_history (user_id, completed, updated_at DESC);

-- Index for server/source queries (find which servers work for an anime)
CREATE INDEX watch_history_anime_source_idx
  ON public.watch_history (anime_id, provider, source);

-- ============================================
-- FAVORITES TABLE
-- ============================================
-- Stores user's favorite anime
