-- Function to get which servers/sources work for a specific anime
-- Returns list of servers that successfully streamed episodes
CREATE OR REPLACE FUNCTION get_working_servers_for_anime(
  p_user_id TEXT,
  p_anime_id TEXT,
  p_provider TEXT DEFAULT 'anilist'
)
RETURNS TABLE (
  source TEXT,
  episode_count BIGINT,
  last_used_at BIGINT
) AS $$
BEGIN
  RETURN QUERY
  SELECT 
    wh.source,
    COUNT(DISTINCT wh.episode_id) as episode_count,
    MAX(wh.updated_at) as last_used_at
  FROM public.watch_history wh
  WHERE wh.user_id = p_user_id
    AND wh.anime_id = p_anime_id
    AND wh.provider = p_provider
  GROUP BY wh.source
  ORDER BY last_used_at DESC;
END;
$$ LANGUAGE plpgsql;

-- Function to get continue watching list (incomplete episodes)
CREATE OR REPLACE FUNCTION get_continue_watching(
  p_user_id TEXT,
  p_limit INTEGER DEFAULT 24
)
RETURNS TABLE (
  anime_id TEXT,
  episode_id TEXT,
  episode_number INTEGER,
  provider TEXT,
  source TEXT,
  watch_position DOUBLE PRECISION,
  duration DOUBLE PRECISION,
  updated_at BIGINT
) AS $$
BEGIN
  RETURN QUERY
  SELECT DISTINCT ON (wh.anime_id, wh.provider)
    wh.anime_id,
    wh.episode_id,
    wh.episode_number,
    wh.provider,
    wh.source,
    wh.position as watch_position,
    wh.duration,
    wh.updated_at
  FROM public.watch_history wh
  WHERE wh.user_id = p_user_id
    AND wh.completed = false
    AND wh.position > 0
    AND wh.position < wh.duration * 0.95
  ORDER BY wh.anime_id, wh.provider, wh.updated_at DESC
  LIMIT p_limit;
END;
$$ LANGUAGE plpgsql;

-- Function to get completed anime
CREATE OR REPLACE FUNCTION get_completed_anime(
  p_user_id TEXT,
  p_limit INTEGER DEFAULT 60
)
RETURNS TABLE (
  anime_id TEXT,
  episode_id TEXT,
  episode_number INTEGER,
  provider TEXT,
  updated_at BIGINT
) AS $$
BEGIN
  RETURN QUERY
  SELECT DISTINCT ON (wh.anime_id, wh.provider)
    wh.anime_id,
    wh.episode_id,
    wh.episode_number,
    wh.provider,
    wh.updated_at
  FROM public.watch_history wh
  WHERE wh.user_id = p_user_id
    AND wh.completed = true
  ORDER BY wh.anime_id, wh.provider, wh.updated_at DESC
  LIMIT p_limit;
END;
$$ LANGUAGE plpgsql;

